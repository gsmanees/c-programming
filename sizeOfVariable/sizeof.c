#include<stdio.h>
int main(){


    // variable declarations
    int i;
    char c;
    float f;
    double d;

    // reading memory allocation sizes of variables using sizeof operator
    printf("\nsize of integer i =%d", sizeof(i));
    printf("\nsize of charactor c =%d", sizeof(c));
    printf("\nsize of float f =%d", sizeof(f));
    printf("\nsize of double d =%d", sizeof(d));

    return 0;

}